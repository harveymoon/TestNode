// this is a simple p5js project that will draw a funny face!


function setup() {
    createCanvas(400, 400);
    }

function draw() {
    background(220);
    fill(255, 0, 0);
    ellipse(200, 200, 200, 200);
    fill(255, 255, 0);
    ellipse(150, 150, 50, 50);
    ellipse(250, 150, 50, 50);
    fill(0, 0, 0);
    ellipse(150, 150, 10, 10);
    ellipse(250, 150, 10, 10);
    fill(0, 0, 0);
    ellipse(200, 200, 10, 10);
    fill(0, 0, 0);
    arc(200, 250, 50, 50, 0, PI);
    }

    